    public void verifyConnect(final MainFrame mainFrame) {
        Runnable r;
        r = new Runnable() {
            
            private boolean disconnected = false;
            
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(5000);
                        System.out.println("Verificando concex√£o...");
                        mainFrame.getChat().ping();
                        if(disconnected){
                            mainFrame.getChat().tryConnect();
                            disconnected = false;
                        }
                    } catch (NotConnectedToServerException | InterruptedException ex) {
                        disconnected = true;
                        mainFrame.setContentPane(new LoginPanel(mainFrame));
                        mainFrame.setVisible(true);
                        Logger.getLogger(MainFrame.class.getName()).log(Level.SEVERE, null, ex);
                    } 
                }
            }
        };

        new Thread(r).start();
    }

